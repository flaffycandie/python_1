# Arithmetic_operators
num1=5
num2=14
print(num1+num2) #addition
print(num1-num2) #subtraction
print(num1*num2) #multiplication
print(num1/num2) #division
print(num1%num2) #modulus/remainder
print(num1**num2) #exponential (17*17)
print(num1//num2) #floor

#Comparison_operators
num3=21
num4=52
print(num3==num4) #equal to
print(num3 !=num4) #not equal to
print(num3 > num4) #greater than
print(num3 < num4) #less than
print(num3 >= num4) #greater than or equal to
print(num3 <= num4) #less than or equal to

#Logical_operators
num5=35
num6=93
num7=150
print(num5 >=2 and num6<=32) #and
print(num5 >=2 or num6<=32) #or
print(not(num5 >=2 and num6<=32)) #not
print(not(num5 >=2 or num6<=32)) #not
